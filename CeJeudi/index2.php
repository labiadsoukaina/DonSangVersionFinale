    <div class="heading text-center" id="don-sang"> 
      <!-- Heading -->
      <h2>Pourquoi donner du sang?</h2>
      <h3>Donner son sang, c'est sauver des vies.</h3>
    </div>
    <div class="row feature design">
      <div class="six columns right">
        <p>Le don de sang est irremplaçable : il n'existe pas aujourd'hui de produit fabriqué artificiellement
        capable de se substituer complétement à lui.
        Chaque jour, 880 dons sont nécessaires.
        Chaque année, 1 million de vies sont sauvées grâce aux dons de sang.</p>
        <p>Le sang est précieux. Le sang est nécessité d’urgence en cas d’accidents, d’opérations ou pour
         les patients cancéreux. Et comme il n’est toujours pas possible à ce jour de fabriquer du sang artificiel, la demande 
         doit être couverte par des dons de sang volontaires. Donnez du sang – vous aussi pourriez en avoir besoin un jour !</p>
        <p>Il est également nécessaire pour les transfusions régulières destinées aux personnes souffrant d’affections comme la thalassémie 
        ou la drépanocytose et sert à fabriquer des produits tels que les facteurs de coagulation pour les sujets atteints d’hémophilie.</p>
        <p>On a constamment besoin d’un approvisionnement régulier en sang parce que ce dernier ne se conserve que pendant une durée limitée 
        avant utilisation. Des dons de sang réguliers par un nombre suffisant de personnes en bonne santé sont nécessaires 
        pour que l’on puisse disposer de sang sécurisé partout et à tout moment.
        Le sang est le don le plus précieux que l’on puisse faire à autrui – c’est un 
        don de vie. En décidant de donner votre sang, vous pouvez sauver une vie, voire plusieurs
        si le sang est séparé en ses constituants – hématies, plaquettes et plasma – qui peuvent être utilisés 
        séparément pour des patients présentant des affections particulières.</p>
        <p>Toute personne en bonne santé, âgée de 18 à 70 ans, ne faisant pas l’objet d’une mesure de protection légale (tutelle…) et reconnue
         apte à l’issue de l’entretien prédon peut donner son sang.Les donneurs de groupe O négatif (O-) sont particulièrement 
         recherchés car leurs globules rouges peuvent être transfusés à tous les patients. Mais les donneurs de tout groupe sanguin
          sont les bienvenus.</p>
        
